def get_folder_for_tid(tid, title="", upper=""):
    """Map a B站 tid to a user folder. Returns (folder_name, confidence)."""
    sub_name, main_name = TID_MAP.get(tid, (None, None))
    if sub_name and sub_name in PARTITION_TO_FOLDER:
        return PARTITION_TO_FOLDER[sub_name], "高"
    
    # Try main partition name as fallback
    main = MAIN_TID_TO_NAME.get(tid)
    if main and main in PARTITION_TO_FOLDER:
        return PARTITION_TO_FOLDER[main], "中"
    
    return None, None


